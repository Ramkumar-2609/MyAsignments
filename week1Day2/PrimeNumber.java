package week1Day2;

public class PrimeNumber {
 
	public static void main(String[] args) {
		 for(int i=5; i<=20; i++)
         {
           if(i%2==0)	 
           {
         
        	 System.out.println("The number "+i+" is not Prime");
         }
         else if(i%3==0)
         {
        	 System.out.println("The number "+i+" is not Prime");
         }
			else
		{
			System.out.println("The number "+i+" is Prime");
		}
	    }
	    }
}
	
